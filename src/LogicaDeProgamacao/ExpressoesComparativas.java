package LogicaDeProgamacao;

public class ExpressoesComparativas {
    public static void main(String[] args) {

        /*
            If e ELSE Se e Se nÃ£o
         */

        // == igual a
        // != Diferente de
        // > Maior que
        // < Menor que
        // >= Maior ou Igual
        // <= Menor ou Igual

        int a = 10;
        int b = 5;

        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a != b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }
}

package LogicaDeProgamacao;

public class SwitchCase {
    public static void main(String[] args) {

        //Switch Case Exemplo

        int idade = 18;

        switch (idade) {
            case 18:
                System.out.println("Maior de Idade");
                break;
            case 17:
                System.out.println("Menor de Idade");
                break;
        }

    }
}
